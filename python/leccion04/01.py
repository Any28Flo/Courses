"""
El ciclo while funciona de igual forma como en los lenguajes de java y de js
En esto la condicion es que mientras una condicion sea verdad esta se ejecutara
Ejercicio
Imprimir del 1 al 3
"""

counter = 0
while counter <= 3:
    print(f"Counter : {counter}")
    counter += 1
else:
    print("fin del ciclo")
