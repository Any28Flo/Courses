# Tipos de datos
# String
nombre = 'Sasha'
print(nombre)
# Para imprimir el tipo se hace con la funcion type
print(type(nombre))
#  Int
age = 3
print(age)
print(type(age))
# Float
peso = 10.32
print(peso)
print(type(peso))

# Booleanos
isActive = False
isValid = True
print(isValid)
print(isActive)
print(type(isValid))
