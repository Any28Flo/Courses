# Operadores aritmeticos
# operador de asignacion -> =
myVar = 3
# Podemos hacer incrementos asignando del lado izquierdo
myVar = myVar + 1
print(myVar)
# forma simplificada
myVar += 1
print(myVar)
# Podemos hacerlo con los diferentes operadores y valores
myVar -= 3
print(myVar)
myVar *= 2
print(myVar)
