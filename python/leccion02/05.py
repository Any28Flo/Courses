# Operadores de comparacion

a = 4
b = 3
# '==' Sirve para comparar si dos valores son iguales
# Lo que va despues del signo de asignacion se llama expresion y puede
# ir entre parentesis (a == b)

resultado = a == b
print(f'Operador ==: {resultado}')

# Diferente que
resultado = a != b
print(f'Diferente que: {resultado}')
# Mayor que
resultado = a > b
print(f'Mayor que {a} > {b} ={resultado}')

# Mayor o igual que
b = 4
resultado = a >= b
print(f'Mayor o igual que {a} >= {b} ={resultado}')

# Menor que

resultado = b < a
print(f'Menor que {a} < {b} ={resultado}')

# Menor o igual que
a = 4
resultado = b <= a
print(f'Menor o igual que {a} <= {b} ={resultado}')
