# Los operadores basicos de pyton son
operadorA = 12
operadorB = 7
# Multiplicacion
resultado = operadorA * operadorB
print(f'Multriplicacion es: {resultado}')
# Division
# La division siempre te la va a dar con flotantes
resultado = operadorA / operadorB
print(f'Division {resultado}')
# Si quieres el resultado con solamente la parte entera
# Utiliza el operador -> //
resultado = operadorA // operadorB
print(f'Division parte entera {resultado}')

# Modulo
resultado = operadorA % operadorB
print(f'Modulo: {resultado}')

# Exponente
resultado = operadorA ** operadorB
print(f'Exponente: {resultado}')